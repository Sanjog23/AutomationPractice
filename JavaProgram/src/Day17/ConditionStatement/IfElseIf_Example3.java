package Day17.ConditionStatement;

public class IfElseIf_Example3 {

	public static void main(String[] args) {
		char ch='z';
		//check given character is vowel or not
		if(ch=='a') {
			System.out.println("Given character is vowel "+ch);
		}else if(ch=='e') {
			System.out.println("Given character is vowel "+ch);
		}else if(ch=='i') {
			System.out.println("Given character is vowel "+ch);
		}else if(ch=='o') {
			System.out.println("Given character is vowel "+ch);
		}else if(ch=='u') {
			System.out.println("Given character is vowel "+ch);
		}else {
			System.out.println("Givien character is not vowel "+ch);
		}		
	}
}