package Dec16th_AllProgramPractice;

public class DisplayNo1To5 {

	public static void main(String[] args) {
		System.out.println("************Print number from 1 to 5*************");

		for (int i = 1; i <= 5; i++) {
			System.out.println(i);
		}
		System.out.println("************Print number from 5 to 1*************");
		for (int x = 5; x >= 1; x--) {
			System.out.println(x);
		}
		System.out.println("************Print number from 127 to 160*************");
		for (int a = 127; a <= 160; a++) {
			System.out.println(a);
		}
	}

}
